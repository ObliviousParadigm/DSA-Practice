#include <bits/stdc++.h>

using namespace std;

int main()
{
    double x = 1.213, y = -1.213;
    
    cout<<x<<endl;
    cout<<"Floor: "<<floor(x)<<endl;
    cout<<"Ceil: "<<ceil(x)<<endl;
    cout<<"Trunc: "<<trunc(x)<<endl;
    cout<<"Round: "<<round(x)<<endl;
    cout<<fixed<<"Setprecision: "<<setprecision(2)<<x<<setprecision(0)<<endl;
    cout<<y<<endl;
    cout<<"Floor: "<<floor(y)<<endl;
    cout<<"Ceil: "<<ceil(y)<<endl;
    cout<<"Trunc: "<<trunc(y)<<endl;
    cout<<"Round: "<<round(y)<<endl;
    cout<<fixed<<"Setprecision: "<<setprecision(2)<<y<<endl;
}
